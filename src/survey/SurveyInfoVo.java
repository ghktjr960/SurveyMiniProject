package survey;

public class SurveyInfoVo {
	private int num;
	private int age;
	private String gender;
	private String area;
	private int metro;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public int getMetro() {
		return metro;
	}
	public void setMetro(int metro) {
		this.metro = metro;
	}
}
